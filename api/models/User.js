    calcLongValue: function (cashflow, currentmonth, lastamount) {
        var newCashflow = cashflow.slice(0, currentmonth);
        var totalval = 0;
        _.each(newCashflow, function (n) {
            if (n < 0) {
                totalval += n;
            }
        });
        return (totalval * -1) + lastamount;
    },
